export function translateText(text: any, targetLang: "ar" | "en" | "fr"): string {
  if (!text) return "";
  if (typeof text !== "string") return String(text);
  if (targetLang === "ar") return text;

  const trimmed = text.trim();
  // Try exact match first
  if (dictionary[trimmed]) {
    return dictionary[trimmed][targetLang] || text;
  }

  // Try stripping typical punctuation or emojis
  const sanitized = trimmed.replace(/[👋🎓🏢🏫🇩🇿⚠️🌙🚀🛠️🛡️📌💼💳✨⚡✕]/g, "").trim();
  if (dictionary[sanitized]) {
    const iconOnly = trimmed.match(/[👋🎓🏢🏫🇩🇿⚠️🌙🚀🛠️🛡️📌💼💳✨⚡✕]/g)?.join(" ") || "";
    const translation = dictionary[sanitized][targetLang];
    return targetLang === "en" ? `${translation} ${iconOnly}` : `${translation} ${iconOnly}`;
  }

  // Fallback: replace common substrings
  let result = text;
  for (const [key, val] of Object.entries(dictionary)) {
    if (key.length > 4 && result.includes(key)) {
      result = result.replaceAll(key, val[targetLang]);
    }
  }
  
  return result;
}

export function AutoTranslate({ children, lang }: { children: React.ReactNode; lang: "ar" | "en" | "fr" }): any {
  if (lang === "ar") {
    return children;
  }

  function translateNode(node: React.ReactNode): React.ReactNode {
    if (node === null || node === undefined) {
      return node;
    }

    if (typeof node === "string") {
      return translateText(node, lang);
    }

    if (typeof node === "number" || typeof node === "boolean") {
      return node;
    }

    if (Array.isArray(node)) {
      return node.map((child, i) => React.createElement(React.Fragment, { key: i }, translateNode(child)));
    }

    if (React.isValidElement(node)) {
      const props = node.props as any;
      if (props && props.children) {
        if (typeof props.children === "function") {
          return node;
        }
        
        try {
          return React.cloneElement(
            node,
            {
              ...props,
              placeholder: props.placeholder && typeof props.placeholder === 'string' ? translateText(props.placeholder, lang) : props.placeholder,
              title: props.title && typeof props.title === 'string' ? translateText(props.title, lang) : props.title,
              label: props.label && typeof props.label === 'string' ? translateText(props.label, lang) : props.label,
            },
            translateNode(props.children)
          );
        } catch (e) {
          return node;
        }
      } else if (props && (props.placeholder || props.title || (props.label && typeof props.label === 'string'))) {
        try {
          return React.cloneElement(node, {
            ...props,
            placeholder: props.placeholder && typeof props.placeholder === 'string' ? translateText(props.placeholder, lang) : props.placeholder,
            title: props.title && typeof props.title === 'string' ? translateText(props.title, lang) : props.title,
            label: props.label && typeof props.label === 'string' ? translateText(props.label, lang) : props.label,
          });
        } catch (e) {
          return node;
        }
      }
    }

    return node;
  }

  return translateNode(children);
}

